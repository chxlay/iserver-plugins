package iserver.plugin.segetter.constants;

/**
 * 字符串常量池
 *
 * @author Alay
 * @date 2022-04-15 23:56
 */
public interface CharStrPool {

    /**
     * 空格
     */
    char SPACE = ' ';

    /**
     * 空格
     */
    String SPACE_STR = " ";
    /**
     * 分号
     */
    char SEMI = ';';

    /**
     * 逗号
     */
    char COMMA = ',';
    /**
     * 点
     */
    char DOT = '.';
    /**
     * 换行
     */
    char LF = '\n';

    /**
     * 花括号（左）
     */
    char DELIM_START = '{';
    /**
     * 花括号（右）
     */
    char DELIM_END = '}';

    /**
     * 花括号（左）
     */
    char BRACKET_START = '(';
    /**
     * 花括号（右）
     */
    char BRACKET_END = ')';

    /**
     * 空字符串
     */
    String EMPTY = "";

    /**
     * new 关键字
     */
    String _new = "new";

}
